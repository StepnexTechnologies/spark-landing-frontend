export type SocialPlatform = keyof typeof SocialIconSVGs;

interface SocialLinkProps {
  platform: SocialPlatform;
  href: string;
  className?: string;
  iconClassName?: string;
}

// Individual social link component
export function SocialLink({ platform, href, className = "", iconClassName = "" }: SocialLinkProps) {
  const isEmail = platform === "email";
  const finalHref = isEmail ? `mailto:${href}` : href;

  return (
    <a
      href={finalHref}
      target={isEmail ? undefined : "_blank"}
      rel={isEmail ? undefined : "noopener noreferrer"}
      className={`text-gray-600 hover:text-[#9747FF] transition-colors ${className}`}
      aria-label={platform.charAt(0).toUpperCase() + platform.slice(1)}
    >
      <span className={iconClassName}>{SocialIconSVGs[platform]}</span>
    </a>
  );
}

interface SocialLinksGroupProps {
  links: Partial<Record<SocialPlatform, string>>;
  variant?: "default" | "circle" | "footer";
  className?: string;
}

// Group of social links
export function SocialLinksGroup({ links, variant = "default", className = "" }: SocialLinksGroupProps) {
  const variantClasses = {
    default: "flex items-center gap-4",
    circle: "flex items-center gap-4",
    footer: "flex items-center gap-4",
  };

  const linkClasses = {
    default: "text-gray-600 hover:text-[#9747FF] transition-colors",
    circle: "w-10 h-10 flex items-center justify-center rounded-full bg-[#F2F2F2] text-[#6B7280] hover:bg-purple-100 hover:text-purple-600 transition-colors",
    footer: "text-gray-400 hover:text-white transition-colors",
  };

  const platforms = Object.keys(links) as SocialPlatform[];

  return (
    <div className={`${variantClasses[variant]} ${className}`}>
      {platforms.map((platform) => {
        const href = links[platform];
        if (!href) return null;

        const isEmail = platform === "email";
        const finalHref = isEmail ? `mailto:${href}` : href;

        return (
          <a
            key={platform}
            href={finalHref}
            target={isEmail ? undefined : "_blank"}
            rel={isEmail ? undefined : "noopener noreferrer"}
            className={linkClasses[variant]}
            aria-label={platform.charAt(0).toUpperCase() + platform.slice(1)}
          >
            {SocialIconSVGs[platform]}
          </a>
        );
      })}
    </div>
  );
}

